# Python Fundamentals Overview

# Welcome to the Python Fundamentals section of the course!
# This module lays the groundwork for all the DevOps automation scripts and tools you'll be writing.

# 1. Language Fundamentals
# - Variables & Data Types: How to create and assign variables, Understanding Python's dynamic typing
# - Comments: Writing inline and block comments for clarity
# - Primitive Types: int, float, bool, str
# - String Manipulation: Concatenation, formatting, slicing, and useful string methods

# 2. Core Data Structures
# - Lists: Ordered, mutable collections
# - Tuples: Ordered, immutable collections
# - Sets: Unordered collections of unique elements
# - Dictionaries: Key-value mappings, highly used in config and API responses

# 3. Control Flow
# - Conditional Logic: if, elif, else statements
# - Loops: for loops for iteration over sequences, while loops for condition-based repetition
# - Loop Control: break to exit loops early, continue to skip to the next iteration

# 4. Efficient Iteration & Data Generation
# - Comprehensions: List, set, and dictionary comprehensions for concise, readable code
# - Built-in Iteration Tools: range() for number sequences, enumerate() for indexed iteration, zip() to combine multiple iterables

# 5. Structuring Code with Functions and Classes
# - Functions: Defining functions using def, Understanding parameters and return values, Using lambda for anonymous functions
# - Variable Arguments: *args for positional argument packs, **kwargs for keyword argument packs
# - Object-Oriented Programming (OOP): Classes and objects, Defining methods, Inheritance basics

